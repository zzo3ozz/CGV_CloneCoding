function focus_sc(a)
{
	var extra = document.getElementsByClassName("s_type");
	var first = document.getElementById("s_type_1");
	for(var i = 0; i <= 3; i++)
	{
		extra[i].style.border = "0";
		extra[i].style.borderBottom = "solid rgb(229, 229, 229) 1px";
		extra[i].style.borderRadius = "0";
	}
	first.style.borderRadius = "0";
	first.style.border = "0";
	first.style.borderTop = "solid rgb(229, 229, 229) 1px";
	a.style.border = "solid rgb(34, 34, 34) 1px";
	a.style.borderRadius = "10px";
	var img = document.getElementById("cinema_img");
	img.src = "https://img.cgv.co.kr//Front/Main/2021/1209/16390080561620.png";
}

